#!/usr/bin/env bash
# Builds statusline.py from src/ modules.
# Run after editing any file in src/claude_code_statusline/

set -e
cd "$(dirname "$0")"

OUT="statusline.py"
SRC="src/claude_code_statusline"

echo "Building $OUT from $SRC/..."

python3 << 'BUILDSCRIPT'
import re
import sys

SRC = "src/claude_code_statusline"
OUT = "statusline.py"

# Module order matters — dependencies first
MODULES = ["themes", "config", "usage", "widgets", "render", "selector", "configure", "main"]

def strip_module(code: str) -> str:
    """Remove relative imports, __future__, and if __name__ blocks."""
    lines = code.split("\n")
    result = []
    skip = False

    for line in lines:
        # Skip 'from __future__ import annotations'
        if line.strip() == "from __future__ import annotations":
            continue

        # Skip relative imports (single-line)
        if re.match(r'^from \.\w+ import \w', line):
            continue

        # Skip multi-line relative imports: from .xxx import (
        if re.match(r'^from \.\w+ import \(', line):
            skip = True
            continue
        if skip:
            if ")" in line:
                skip = False
            continue

        # Skip standalone 'import tty' and 'import termios' — will be handled by platform check
        # (keep them — the platform-specific _read_key already handles this)

        # Skip if __name__ == "__main__" blocks at module level
        if re.match(r'^if __name__\s*==\s*["\']__main__["\']', line):
            break  # stop reading this module

        result.append(line)

    # Remove leading/trailing blank lines
    while result and not result[0].strip():
        result.pop(0)
    while result and not result[-1].strip():
        result.pop()

    return "\n".join(result)


# ── Build ────────────────────────────────────────────

parts = []

# Header
parts.append('''#!/usr/bin/env python3
"""Claude Code Statusline — Beautiful, customizable status bar.

Single-file distribution. Generated from src/ modules by build.sh.
Do NOT edit this file directly — edit src/claude_code_statusline/ and run ./build.sh

https://github.com/Lucagdev/claude-code-statusline
"""

from __future__ import annotations

import json
import os
import sys
import re
import time
import tempfile
import argparse
import shutil
from dataclasses import dataclass, field
from pathlib import Path
from urllib.request import Request, urlopen
from urllib.error import URLError, HTTPError
from datetime import datetime, timezone

# Platform-specific key reading
if sys.platform == "win32":
    import msvcrt
else:
    import tty
    import termios
''')

# Each module
for mod in MODULES:
    filepath = f"{SRC}/{mod}.py"
    try:
        with open(filepath) as f:
            code = f.read()
    except FileNotFoundError:
        print(f"  SKIP {filepath} (not found)", file=sys.stderr)
        continue

    stripped = strip_module(code)

    # Remove any top-level import lines that we already have in the header
    # (json, sys, os, re, time, tempfile, argparse, shutil, dataclass, Path, etc.)
    import_lines = {
        "import json", "import os", "import sys", "import re", "import time",
        "import tempfile", "import argparse", "import shutil",
        "from dataclasses import dataclass", "from dataclasses import dataclass, field",
        "from pathlib import Path", "from urllib.request import Request, urlopen",
        "from urllib.error import URLError, HTTPError",
        "from datetime import datetime, timezone",
        "import tty", "import termios",
    }
    filtered_lines = []
    for line in stripped.split("\n"):
        if line.strip() in import_lines:
            continue
        # Also skip 'from dataclasses import ...' variants
        if re.match(r'^from dataclasses import ', line.strip()):
            continue
        if re.match(r'^from pathlib import ', line.strip()):
            continue
        if re.match(r'^from urllib', line.strip()):
            continue
        if line.strip() in ("import tty", "import termios"):
            continue
        filtered_lines.append(line)
    stripped = "\n".join(filtered_lines)

    parts.append(f"""
# {'=' * 65}
# Module: {mod}
# {'=' * 65}

{stripped}""")

# Entry point
parts.append("""

# ═══════════════════════════════════════════════════════════════════
# Entry point
# ═══════════════════════════════════════════════════════════════════

if __name__ == "__main__":
    entry()
""")

output = "\n".join(parts)

# Fix: remove ALL remaining inline relative imports (from .xxx import yyy)
# Replace with empty line to preserve indentation of surrounding code
# Handle both single-line and multi-line inline imports
cleaned_lines = []
skip_inline = False
for line in output.split("\n"):
    stripped = line.lstrip()
    if skip_inline:
        if ")" in line:
            skip_inline = False
        continue
    if stripped.startswith("from .") and " import " in stripped:
        if stripped.endswith("("):
            skip_inline = True
        # Replace with pass or blank to preserve indentation
        continue
    cleaned_lines.append(line)
output = "\n".join(cleaned_lines)

# Fix: tui_main() reference → main() won't work since configure.main shadows it
# Replace the _open_config function's call
output = output.replace(
    "    tui_main()",
    "    builder = Builder()\n    builder.run()"
)

# Write
with open(OUT, "w") as f:
    f.write(output)

print(f"  Written {OUT}")
BUILDSCRIPT

# Validate syntax
python3 -c "import py_compile; py_compile.compile('$OUT', doraise=True)" 2>&1

# Quick smoke test
echo '{"model":{"display_name":"Test"}}' | python3 "$OUT" > /dev/null 2>&1 && echo "  Smoke test passed" || echo "  Smoke test FAILED"

LINES=$(wc -l < "$OUT")
echo "✓ Built $OUT ($LINES lines)"
