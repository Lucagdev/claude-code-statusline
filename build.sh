#!/usr/bin/env bash
# Builds statusline.py from src/ modules.
# Run after editing any file in src/claude_code_statusline/

set -e
cd "$(dirname "$0")"

OUT="statusline.py"

echo "Building $OUT from src/..."

cat > "$OUT" << 'HEADER'
#!/usr/bin/env python3
"""Claude Code Statusline — Beautiful, customizable status bar.

Single-file distribution. Generated from src/ modules by build.sh.
Do NOT edit this file directly — edit src/claude_code_statusline/ and run ./build.sh

https://github.com/Lucagdev/claude-code-statusline
"""
HEADER

# Append each module, stripping relative imports and module docstrings
for mod in themes config usage widgets render configure main; do
    FILE="src/claude_code_statusline/${mod}.py"
    if [ ! -f "$FILE" ]; then
        echo "  SKIP $FILE (not found)"
        continue
    fi
    echo "" >> "$OUT"
    echo "# ═══════════════════════════════════════════════════════════════" >> "$OUT"
    echo "# Module: $mod" >> "$OUT"
    echo "# ═══════════════════════════════════════════════════════════════" >> "$OUT"
    echo "" >> "$OUT"

    python3 -c "
import re, sys

with open('$FILE') as f:
    code = f.read()

# Remove 'from __future__ import annotations' (added once at top)
code = code.replace('from __future__ import annotations\n', '')

# Remove relative imports (from .xxx import yyy)
code = re.sub(r'^from \.\w+ import .*\n', '', code, flags=re.MULTILINE)

# Remove 'if __name__' blocks at end of individual modules
code = re.sub(r'\nif __name__\s*==\s*.*?$.*', '', code, flags=re.DOTALL | re.MULTILINE)

sys.stdout.write(code)
" >> "$OUT"
done

# Add __future__ annotations at top (after shebang+docstring)
python3 -c "
with open('$OUT') as f:
    content = f.read()

# Insert after the closing triple-quote of the docstring
idx = content.find('\"\"\"', content.find('\"\"\"') + 3) + 3
content = content[:idx] + '\n\nfrom __future__ import annotations\n' + content[idx:]

# Add entry point at the bottom
content += '''

# ═══════════════════════════════════════════════════════════════
# Entry point
# ═══════════════════════════════════════════════════════════════

if __name__ == \"__main__\":
    entry()
'''

with open('$OUT', 'w') as f:
    f.write(content)
"

# Validate
python3 -c "import py_compile; py_compile.compile('$OUT', doraise=True)" 2>&1

LINES=$(wc -l < "$OUT")
echo "✓ Built $OUT ($LINES lines)"
